const getImage = (kind, item, status) => {
    if (kind === 'CAR') {
        return getForStatus(item, status, vehicleType[item.vehicleType] || car_other);
    } else if (kind === 'PLANE') {
        return getPlaneImage(item, status, craftModelNo[item.craftModelNo] || plane_B);
    } else if (kind === 'PERSON') {
        return getPersonImage(item, status, personDept[item.deptId] || person_other)
    }
}

