const getPersonImage = (item, status, obj) => {
    if (status === 'SELECT') {
        return obj.select;
    }
    return item.status === 'MISSING' ? obj.missing : obj.person;
}

const getForStatus =  (item, status, obj) => {
    if (status === 'SELECT') {
    	return obj.select;
    }
    if (item.warInfo) {
    	return obj.abn;
    }
    return item.status === 'MISSING' ? obj.missing : obj.car;
}

const inOut = (item, obj, status) => {
    // 默认没有flightStatus字段 为出港状态
    if (item.flightStatus === 'ArrFlight') {
        return obj[status];
    } else{
        return obj[`${status}Out`];
    }
}

const getPlaneImage = (item, status, obj) => {
    if (status === 'SELECT') {
        return inOut(item, obj, 'select');
    }

    if (item.arrKeyGuarantee != '') {
        return obj.planeKey;
    } else if (item.depKeyGuarantee != '') {
        return obj.planeKeyOut;
    }

    // 判断延误状态
    if (item.arrAbnStatus === '延误') {
        return obj.delay;
    } else if (item.depAbnStatus === '延误'){
        return obj.delayOut;
    }

    if (item.status === 'MISSING') {
        return inOut(item, obj, 'missing');
    } else {
        return inOut(item, obj, 'plane');
    }

}
